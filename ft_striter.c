#include "libft.h"

char	*ft_striter(char *s, int (*f)(int))
{
	size_t			i;

	if (s == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (s[i] != '\0')
	{
		if (f != NULL)
		{
			s[i] = f(s[i]);
		}
		++i;
	}
	return (s);
}
